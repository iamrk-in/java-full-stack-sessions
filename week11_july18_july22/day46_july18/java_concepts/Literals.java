public class Literals {
    public static void main(String[] args) {
        // integer literal
        int i;
        i = 10;

        byte b = 100;
        short s = 200;
        int num2 = 1331313;
        long l = 63477246L;

        // float literal
        double d = 23.4;
        float f = 23.6f;

        // character literal
        char ch;
        ch = 'D';
        System.out.println(ch);

        // string literal
        String str = "Java Literals";
    }
}
